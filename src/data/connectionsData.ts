// Function to get today's connections game
export const getTodaysConnectionsGame = (): ConnectionsGameData | null => {
  const today = new Date().toISOString().split('T')[0];
  return connectionsGamesData.find(game => game.date === today) || connectionsGamesData[0];
};

// Function to get connections game by ID
export const getConnectionsGameById = (gameId: string): ConnectionsGameData | null => {
  return connectionsGamesData.find(game => game.id === gameId) || null;
};

// Function to get all connections games (for archive)
export const getAllConnectionsGames = (): ConnectionsGameData[] => {
  return connectionsGamesData.sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
};
